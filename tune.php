function findData($params, $cars)
{
    foreach ($cars['audi'] as $car) {
        if ($car['modelName'] === $params['model']) {
            foreach ($car['generations'] as $generation) {
                if ($generation['genName'] === $params['gen']) {
                    foreach (['gasolineEngines', 'dieselEngines'] as $engineType) {
                        foreach ($generation['engines'][$engineType] as $engine) {
                            if ($engine['engineName'] === $params['engine']) {
                                return $engine['data'] ?? null;
                            }
                        }
                    }
                }
            }
        }
    }
    return null;
}

function references($data)
{
    $dom = new DOMDocument;
    @$dom->loadHTML('<?xml encoding="UTF-8">' . $data, LIBXML_HTML_NOIMPLIED | LIBXML_HTML_NODEFDTD);

    $data = str_replace('UPstage', '', $data);

    $xpath = new DOMXPath($dom);
    $h1 = $xpath->query('//h1[@class="hidden"]')->item(0);
    if ($h1) {
        $h1->parentNode->removeChild($h1);
    }

    $li = $xpath->query('//li[@class="visible-xs"]')->item(0);
    if ($li) {
        $li->parentNode->removeChild($li);
    }

    $morePopups = $xpath->query('//*[@id="more_popup"]');
    foreach ($morePopups as $morePopup) {
        $morePopup->parentNode->removeChild($morePopup);
    }

    $morePopupsLink = $xpath->query('//*[@href="#more_popup"]');
    foreach ($morePopupsLink as $morePopup) {
        $morePopup->parentNode->removeChild($morePopup);
    }

    $pricePopupsLink = $xpath->query('//*[@href="#popup_price"]');
    foreach ($pricePopupsLink as $link) {
        $span = $dom->createElement('span', 'По запросу');
        $link->parentNode->replaceChild($span, $link);
    }

    $reviewForms = $xpath->query('//form[contains(@class, "review_car")]');
    foreach ($reviewForms as $form) {
        $form->parentNode->removeChild($form);
    }

    $tab2Divs = $xpath->query('//div[contains(@class, "tab2")]');
    foreach ($tab2Divs as $tab2Div) {
        $tab2Div->parentNode->removeChild($tab2Div);
    }

    $availableOptions = $xpath->query('//*[@class="available-options"]');
    if ($availableOptions->length > 0) {
        foreach ($availableOptions as $options) {
            $options->parentNode->removeChild($options);
        }

        $lastMenuItem = $xpath->query('//aside[@class="left-menu"]/ul/li[last()]')->item(0);
        if ($lastMenuItem) {
            $lastMenuItem->parentNode->removeChild($lastMenuItem);
        }
