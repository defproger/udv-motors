    $tabPhotos = $xpath->query('//div[contains(@class, "tab")]//img');
    foreach ($tabPhotos as $photo) {
        $photo->parentNode->removeChild($photo);
    }

    $mediaFiles = $xpath->query('//div[contains(@class, "media-files")]');
    foreach ($mediaFiles as $mediaFile) {
        $mediaFile->parentNode->removeChild($mediaFile);
    }

    return $dom->saveHTML();
}

